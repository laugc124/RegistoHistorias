    private void cbCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCrearActionPerformed
        try{
        String titulo = txtTitulo.getText();
        String descripcion_corta = txtDescripcion.getText();
        String tipo_historia = CBtipohistoria.getSelectedItem().toString();
        String historia= txthistory.getText();
        String autor = txtAutor.getText();
        String fecha = txtFecha.getText();
        
        
        
        
        if (titulo.equals("")|| descripcion_corta.equals("")||historia.equals("")||autor.equals("")||fecha.equals("")){
            JOptionPane.showMessageDialog(this, "completar todos los campos");
        }else{
            Clshistory history = new Clshistory(0, titulo, descripcion_corta, tipo_historia, historia, autor, fecha);
            ctlhistory.Createhistory(history);
            //historyObjectList.add(history);
            this.Filllist();
            this.clearhistoryfields();
            JOptionPane.showMessageDialog(this, "historia guardada correctamente");
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "revisar historia");
        }

    }//GEN-LAST:event_cbCrearActionPerformed

    private void cbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbBuscarActionPerformed
        
        String titulo = txtTitulo.getText();
        Clshistory history =(Clshistory) ctlhistory.Searchhistorybytitulo(titulo, "historias reales");
        if (history == null){
            JOptionPane.showMessageDialog(this, "historia NO encontrada");
        }else{
            txtidhistory.setText(history.getId()+"");
            txtDescripcion.setText(history.getDescripcion_corta());
            CBtipohistoria.setSelectedItem(history.getTipoHistoria());
            txthistory.setText(history.getHistoria());
            txtAutor.setText(history.getAutor());
            txtFecha.setText(history.getFecha());
            txtTitulo.setText(history.getTitulo());                 
            }
        
        
        
        
        
    }//GEN-LAST:event_cbBuscarActionPerformed

    private void cbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbEditarActionPerformed
        
        String titulo = txtTitulo.getText();
        String descripcion_corta = txtDescripcion.getText();
        String tipo_historia = CBtipohistoria.getSelectedItem().toString();
        String historia= txthistory.getText();
        String autor = txtAutor.getText();
        String fecha = txtFecha.getText();
        
                
        try{
            int  historyid = Integer.parseInt(txtidhistory.getText());
            Clshistory history = new Clshistory(historyid, titulo, descripcion_corta, tipo_historia, historia, autor, fecha);
            
            
            boolean response = ctlhistory.Edithistory(history);
            
            if(response){
                this.clearhistoryfields();
                JOptionPane.showMessageDialog(this, "informacion actualizada");
                }else{
                    JOptionPane.showMessageDialog(this, "error en la actualizacion");
                }
                }catch (Exception e){
                    JOptionPane.showMessageDialog(this, "cambio no guardado");
                }
                
                
            
            
            
        
       
        
        
        
    }//GEN-LAST:event_cbEditarActionPerformed

    private void cbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbEliminarActionPerformed
            String titulo = txtTitulo.getText();
            
                
        try{
                                            
                boolean response = ctlhistory.Deletehistory(titulo);
            
            if(response){
                this.clearhistoryfields();
                JOptionPane.showMessageDialog(this, "informacion eliminada");
                }else{
                    JOptionPane.showMessageDialog(this, "informacion no eliminada");
                }
                }catch (Exception e){
                    JOptionPane.showMessageDialog(this, "cambio no realizado");
                }
    }//GEN-LAST:event_cbEliminarActionPerformed

