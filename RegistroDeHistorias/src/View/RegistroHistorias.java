    private void clearhistoryfields(){
        txtTitulo.setText("");
        txtDescripcion.setText("");
        txthistory.setText("");
        txtAutor.setText("");
        txtFecha.setText("");
        CBtipohistoria.setSelectedItem(0);
    }
    
    private void Filllist(){
        DefaultListModel model = new DefaultListModel();
        int index =0;
        for (Clshistory history : historyObjectList) {
            model.add(index, history);
            index++;
            
        }
    }
